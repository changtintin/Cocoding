<h3 id = "instruction_title">Hesitate to Sign up?</h3>
<h5>
    Whether you've joined our platform or not, 
    here's a quick guide to help you get started and make the most of your experience:
</h5>
<ol>
    <li class="instruc-li">
        <b>Explore the Content:</b><br>
        Take some time to browse through the posts, articles, or any other type of content 
        available on the platform. Discover interesting topics, inspiring stories, 
        or informative discussions that capture your interest.
    </li>
    <li class="instruc-li">
        <b>Like and Dislike:</b><br>
        When you come across a post that resonates with you, feel free to 
        express your appreciation by liking it. On the other hand, if a post doesn't align with your preferences, 
        you can dislike it. Your likes and dislikes contribute to the overall popularity and visibility of the content.
    </li>
    <li class="instruc-li">
        <b>Engage with Comments:</b> <br>
        If you find a post intriguing or thought-provoking, leave a comment to share your thoughts
        or engage in a conversation with other users.
        You can also check the comments section of your own posts to respond to feedback or 
        interact with fellow community members.
    </li>
    <li class="instruc-li">
        <b>Post Your Articles:</b><br> 
        Share your unique insights, stories, or expertise by creating and 
        posting your own articles. This is an opportunity for you to showcase your creativity and contribute 
        valuable content to the platform. Feel free to explore various topics and start meaningful discussions.</li>
    <li class="instruc-li">
        <b>Connect with Others:</b><br> 
        Engage with the community by connecting with other users. Follow users whose content you enjoy 
        and build connections through likes, comments, and discussions. Networking and engaging 
        with like-minded individuals can be rewarding and expand your knowledge base.
    </li>
    <li class="instruc-li">
        <b>Personalize Your Profile: </b><br>
        Customize your profile to reflect your personality and interests. 
        Add a profile picture, write a short bio, and provide any additional information you'd like to share. 
        A personalized profile helps other users get to know you better and facilitates meaningful connections.
    </li>
    <li class="instruc-li">
        <b>Stay Updated:</b><br>
        Keep an eye on notifications, updates, or newsletters from the platform. 
        These will inform you about new content, trending discussions, or any announcements related to the community. 
        Staying updated ensures that you don't miss out on exciting opportunities or important updates.
    </li>
</ol>


<h5>
    Remember, this guide is just a starting point. 
    Feel free to explore, experiment, and contribute to the platform in ways that align with your interests and goals. 
    We're thrilled to have you on board and look forward to witnessing your contributions and engagement within our community!
</h5>
